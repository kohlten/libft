void	*ft_memset(void *b, int c, size_t len)
{
	char			*d;
	unsigned char	ch;

	ch = c;
	d = b;
	if (len)
	{
		while (len)
		{
			*d = ch;
			len--;
			d++;
		}
	}
	return (b);
}
