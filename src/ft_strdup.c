#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		len;
	char	*result;
	int		i;

	len = ft_strlen(s1) + 1;
	result = ft_memalloc(sizeof(char) * len);
	if (!result)
		return (NULL);
	i = 0;
	while (i < len)
	{
		result[i] = s1[i];
		i++;
	}
	return (result);
}
