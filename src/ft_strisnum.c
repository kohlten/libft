#include "libft.h"

int	ft_strisnum(char *str)
{
	int i;

	i = 0;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '-')
		i++;
	else if (str[i] == '+')
		i++;
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
		i++;
	return ((str[i] >= '0' && str[i] <= '9') || str[i] == '\0');
}
